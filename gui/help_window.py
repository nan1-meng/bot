# 文件路径: gui/help_window.py
import tkinter as tk
from tkinter import scrolledtext

class HelpWindow:
    def __init__(self, parent):
        self.window = tk.Toplevel(parent)
        self.window.title("交易机器人使用文档")
        self.window.geometry("800x600")
        self.window.transient(parent)
        self.window.grab_set()

        self.window.update_idletasks()
        x = parent.winfo_x() + (parent.winfo_width() - self.window.winfo_width()) // 2
        y = parent.winfo_y() + (parent.winfo_height() - self.window.winfo_height()) // 2
        self.window.geometry(f"+{x}+{y}")

        text_area = scrolledtext.ScrolledText(
            self.window,
            wrap=tk.WORD,
            font=("微软雅黑", 10),
            padx=10,
            pady=10
        )
        text_area.pack(fill=tk.BOTH, expand=True)

        doc_content = """\
═══════════════════════════════════════════════════════════════
                Bybit/Binance 交易机器人使用手册（自适应动态策略版）
═══════════════════════════════════════════════════════════════

【一、添加 API Key】
────────────────────────────────────────────────────────────
1. 点击左侧“添加Key”按钮。
2. 选择平台（Bybit 或 Binance）。
3. 填写 Key 标识（自定义名称，如“主账户”）。
4. 填入 API Key 和 Secret Key（需要交易所 API 权限：读取、现货交易）。
5. 确定后，系统会自动开始监控余额和价格。

【二、添加币种并配置策略】
────────────────────────────────────────────────────────────
1. 在左侧 Key 列表中选择一个已添加的 Key。
2. 在中间币种列表中，右键点击未配置的币种 → “添加到配置”。
3. 右侧配置面板自动切换到该币种的参数。
4. 选择模式（策略模式 / AI模式 / 刷单模式），调整参数。
5. 点击“启动...模式到选中币种”按钮启动机器人。

【三、三种模式详解】
────────────────────────────────────────────────────────────
▸ 策略模式（适合稳健交易）
   - 基于 RSI、布林带、MACD、均线等指标组合判断买卖点。
   - 可自定义止盈止损、仓位比例。
   - 支持趋势过滤和 RSI 阈值开关。

▸ AI模式（原名资深快卖，现已升级为自适应动态策略）
   - 自动分析市场状态（趋势、震荡、高波动），动态调整策略参数。
   - 内置多时间框架过滤（1h/4h/1d），避免逆势交易。
   - 基于 ATR 的动态仓位管理和止损止盈。
   - 智能补仓（次数递减，条件触发）。
   - 健康度评分系统：根据历史交易表现调整币种权重。
   - 支持分仓卖出和全仓卖出条件（盈利、RSI、成交量等）。
   - 连续亏损自动暂停买入，避免情绪化交易。

▸ 刷单模式（适合做市、刷交易量）
   - 设定每笔买入金额、目标总交易额、奖励币数量。
   - 自动买入后立即卖出，循环直至达到目标交易额。
   - 可设置卖出间隔，避免频率过高。
   - 支持盈利卖出模式（买入后等待盈利再卖出）。

【四、全局模式（自动选币+自动交易）】
────────────────────────────────────────────────────────────
- 在主界面配置面板中，选择“全局模式”并设置参数（最大买入金额、最大补仓金额、买入评分阈值等）。
- 启动全局模式后，系统会自动扫描所有 USDT 交易对，筛选出符合条件的币种（基于成交量、波动率、余额等）。
- 筛选出的币种进入候选池，系统实时监控并自动买入。
- 买入后自动创建卖出机器人（使用与 AI 模式相同的动态分仓策略）。
- 全局模式支持动态市场状态调整、健康度管理、风险控制。
- 遇到权限错误或长期表现差的币种自动加入黑名单。

【五、强制卖出】
────────────────────────────────────────────────────────────
- 在币种列表上右键点击任意币种 → “强制卖出”。
- 立即市价卖出该币种所有持仓，不经过任何策略，并自动停止该币种机器人。
- 强制卖出的交易会记录在“交易记录”中，模式显示为“manual/force_sell”。

【六、交易记录管理】
────────────────────────────────────────────────────────────
- 用户菜单 → 交易记录。
- 可按 Key、模式、币种、方向、时间范围筛选。
- 支持全选、反选、批量删除、导出 CSV 文件。
- 盈亏采用 FIFO（先进先出）方式计算，准确反映每笔卖出的收益。
- 导出 CSV 时订单号自动格式化为文本，避免科学计数法。

【七、日志面板】
────────────────────────────────────────────────────────────
- 日志自动滚动：滚动到底部时新日志自动跟随，否则停留在当前浏览位置。
- 不同级别日志用颜色区分：
  • 绿色：盈利信息
  • 红色：亏损或错误
  • 金色：买入卖出动作

【八、常见问题】
────────────────────────────────────────────────────────────
Q: 为什么 AI 模式或全局模式不交易？
A: 请检查市场状态（可能是熊市或高波动导致阈值提高），或币种健康度过低。可通过日志查看评分和阈值。

Q: 全局模式如何筛选币种？
A: 系统会每小时刷新候选池，过滤条件：24h成交量>100万U、24h波动>0.5%、余额足够、未在黑名单等。

Q: 如何查看币种健康度？
A: 健康度由系统自动根据历史交易表现计算（0-100），可在日志或数据库表中查看。

Q: 补仓逻辑是怎样的？
A: 最多补仓3次，金额递减（50%/30%/20%），触发条件：浮亏超过0.5×ATR或跌幅>1.5×ATR，且市场非熊市。

Q: 如何自定义风险参数？
A: 可在配置面板中调整“最大买入金额”、“最大补仓金额”，其余风险参数由系统自适应。

【九、安全提示】
────────────────────────────────────────────────────────────
- 请勿将 API Key 泄露给他人，建议设置 IP 白名单。
- 机器人只进行现货交易，不会操作杠杆或合约。
- 建议先用小额资金测试，熟悉策略后再投入大额资金。

═══════════════════════════════════════════════════════════════
                        版本：4.0 ｜ 最后更新：2026-03-31
═══════════════════════════════════════════════════════════════
"""
        text_area.insert(tk.END, doc_content)
        text_area.config(state=tk.DISABLED)